let animal = prompt ("Inserisci il numero del tuo animale preferito: 1.Dog 2.Cat 3.Goldfish 4.Monkey  5.Panda")

switch (animal) {
    case 1 :
        alert ("Choose the dog");
        break;
 
    case 2 :
        alert ("Choose the Cat");
        break;

    case 3 :
        alert ("Choose the Goldfish");
        break;
 
    case 4:
        alert ("Choose the Monkey");
        break;

    case 5 :
        alert ("Choose the Panda");
        break;

default:

        alert ("Animal not found");
        break;
}
